@pytest.mark.asyncio
@pytest.mark.parametrize("score,expected_tier,expected_priority", [
    pytest.param(40, "l2_support", "normal", id="score-40"),
    pytest.param(59, "l2_support", "normal", id="score-59"),
    pytest.param(60, "manager", "high", id="score-60"),
    pytest.param(79, "manager", "high", id="score-79"),
    pytest.param(80, "senior_manager", "urgent", id="score-80"),
    pytest.param(100, "senior_manager", "urgent", id="score-100"),
])
async def test_escalation_tier_boundaries(score, expected_tier, expected_priority):
    """
    Parametrized boundary test for escalation_assessment before_call tier logic.
    Directly invokes the wrapper to validate thresholds without an LLM call.

    Input:
        Synthetic state with escalation_score set to boundary value.

    Expected flow:
        EscalationAssessmentWrapper.before_call(state) — no LLM

    Expected state:
        escalation_tier and case_priority match the tier table.
    """
    from customer_service_assistant_v1 import EscalationAssessmentWrapper, StateManager

    class MockAgent:
        async def __call__(self, msg):
            return type("Msg", (), {"get_text_content": lambda self: "mock"})()

    state = StateManager()
    state.set("escalation_score", score)
    state.set("case_priority", "normal")
    state.set("escalation_tier", "")

    wrapper = EscalationAssessmentWrapper(MockAgent(), state, resolve_impl=None)
    await wrapper.before_call(None)

    assert state.get("escalation_tier") == expected_tier, (
        f"score={score}: expected tier '{expected_tier}', got '{state.get('escalation_tier')}'"
    )
    assert state.get("case_priority") == expected_priority, (
        f"score={score}: expected priority '{expected_priority}', got '{state.get('case_priority')}'"
    )